import Foundation

infix operator <
infix operator >

// FUTUREFIX: comparison operators with optionals were removed from the Swift Standard Library.
// Consider refactoring the code to use the non-optional operators.
func < <T: Comparable>(lhs: T?, rhs: T?) -> Bool {
    switch (lhs, rhs) {
    case let (l?, r?):
        return l < r
    case (nil, _?):
        return true
    default:
        return false
    }
}

// FUTUREFIX: comparison operators with optionals were removed from the Swift Standard Library.
// Consider refactoring the code to use the non-optional operators.
func > <T: Comparable>(lhs: T?, rhs: T?) -> Bool {
    switch (lhs, rhs) {
    case let (l?, r?):
        return l > r
    default:
        return rhs < lhs
    }
}
